// Запросить у пользователя
// пятиразрядное число и определить, является ли оно палиндромом.
const palindrom = (number) => { // создаем функцию с параметром number
    if ((number > 9999) && (number < 100000)) { // задаем условие. если число больше 9999 и меньше 100000 (все числа в этом диапазоне пятизначные)
            //на этой строчке может показаться, что происходит много всего
            // число палиндром - это число, которое если развернуть, то получится то же самое (например 101)
            let result  = number.split('').reverse().join('') == number; // создаем переменную result
                                                                         // переменной присваиваем значение
                                                                         // .split('') добавляет запятую после каждого символа, то есть приводит переменную к виду массива
                                                                         // а массив уже можно развернуть, отразить, то есть, если было бы 1234, то стало бы 4321
                                                                         // это делает .reverse()
                                                                         // а .join('), убирает запятые, которые добавил .split('')
                                                                         // и последнее, что мы делаем, мы сравниваем, равно ли полученное число тому, которое мы вводили
                                                                         // если перевернутое число точно такое же, как мы ввели, то в переменную result запишется значение true. то есть истина
            if (result) { // задаем условие. если переменная result истиная, то вернуть палиндром.
                          // если у переменной значение true, то переменная истинная, а если false, то значение антиистинное
                return('палиндром')
            } else { // в остальных случаях это не палиндром. еще мы можем записать это как "else if(!result)", в этом случае, восклицательный знак бы означал если перерменная антиистина, то есть false
                return('не палиндром')
            }

    } else { // если введенное число не вошло в диапазон  ((number > 9999) && (number < 100000)), то его кидает сюда и возвращает, что число не пятизначное
        return ("Вы ввели не пятизначное");
    }
}

alert( //вызываем вспылвающее окно
    palindrom( // в всплывающем окне отображаем результат функции palindrom
        prompt("Введите пятизначное число") // и в качестве аргумента функции, того самого 5 значного числа, передаем значение prompt. а эта штука в свою очередь попросит пользователя ввсти число (надеюсь, понятно))
    )
)


