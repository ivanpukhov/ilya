//Запросить у пользователя число от 0 до 9 и вывести ему
// спецсимвол, который расположен на этой клавише (1–!,
//     2–@, 3–# и т. д).


const spec = (a) => {  //создаем функцию с параметром a
    // Конструкция switch заменяет собой сразу несколько if.
    // Она представляет собой более наглядный способ сравнить выражение сразу с несколькими вариантами.
    switch (a) { // берем наш параметр и сравниваем
        case '1': // если a = 1
            return ('!'); // то выводим восклицательный знак

        case '2': // если a = 2
            return ('@'); // то выводим @

        case '3': // если a = 3
            return ('#'); // то выводим #
        // и так далее
        case '4':
            return ('$');

        case '5':
            return ('%');

        case '6':
            return ('^');

        case '7':
            return ('&');

        case '8':
            return ('*');

        case '9':
            return ('(');

        case '0':
            return (')');

        default: // если а не равно ни одному из этих символов, значит пользователь ввел не цифру
            return ('вы ввели не цифру')
    }
}

alert( //вызываем вспылвающее окно
    spec(// в всплывающем окне отображаем результат функции spec
        prompt('Введите цифру')// и в качестве аргумента функции,  передаем значение prompt. а эта штука в свою очередь попросит пользователя ввести число
    )
)