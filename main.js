// Создать функцию, которая принимает 3 значения (a,b,c)
// а результатом выполнения будет формула (a^2*b^2)/c^2
const func = (a, b, c) => {// тут все просто. получаем 3 параметра функции, a, b и с, и возвращаем выражение
    // в javascript  возвести в степень - это **
    // у нас получается выражение (a ** 2 * b ** 2) / c ** 2
    return ((a ** 2 * b ** 2) / c ** 2)// его и возвращаем
}
// вызываем функцию с помощью alert (всплывающее сообщение на сайте)
alert(
    func(2, 2, 2)
)

